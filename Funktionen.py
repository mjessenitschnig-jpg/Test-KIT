

# def ist ein Schlüsselwort, um eine Funktion zu definieren. Es wird gefolgt von einem Funktionsnamen und Klammern, die Parameter enthalten können. Der Codeblock innerhalb der Funktion wird eingerückt.
def say_hello(first_name, last_name):
    print("Hallo " + first_name + " " + last_name)
    # first_name und last_name sind Parameter, die die Vornamen und Nachnamen enthalten, die an die Funktion übergeben werden. Wenn die Funktion aufgerufen wird, werden die Werte der Parameter in die Funktion eingefügt und ausgeführt.
    print("Willkommen zurück")

say_hello("Max", "Mustermann")
# In diesem Beispiel wird die Funktion say_hello definiert, die zwei Parameter (first_name und last_name) akzeptiert. Die Funktion gibt eine Begrüßungsnachricht aus, die den vollständigen Namen enthält, und eine Willkommensnachricht. Schließlich wird die Funktion mit den Argumenten "Max" und "Mustermann" aufgerufen, um die Ausgabe zu erzeugen.


