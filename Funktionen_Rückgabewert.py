
def maximum(a, b):
    if a < b:
        return b
    else:
        return a
    #return wenn a kleiner als b ist, dann gebe b zurück, sonst gebe a zurück

result = maximum(10, 7)    
print(result)
#result ist eine Variable, die den Rückgabewert der Funktion maximum speichert. In diesem Fall wird die Funktion mit den Argumenten 3 und 7 aufgerufen, und da 3 kleiner als 7 ist, gibt die Funktion 7 zurück. Das Ergebnis wird dann in der Variable result gespeichert und ausgegeben.
